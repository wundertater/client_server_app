<template>
  <transition name="fade">
    <div class="modal-overlay" @click.self="emit('close')">
      <div class="modal-card">
        <h3>–†–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –ø—Ä–µ–ø–æ–¥–∞–≤–∞—Ç–µ–ª—è</h3>

        <div v-if="loading" class="loading">–ó–∞–≥—Ä—É–∑–∫–∞...</div>

        <div v-else-if="form" class="instructor-card">
          <!-- –§–æ—Ç–æ -->
          <div class="photo-block">
            <div
              v-if="photoPreview"
              class="photo"
              :style="{ backgroundImage: `url(${photoPreview})` }"
            ></div>
            <div v-else class="photo placeholder">–ù–µ—Ç —Ñ–æ—Ç–æ</div>

            <input type="file" accept="image/*" @change="onPhotoSelected" />
          </div>

          <!-- –û—Å–Ω–æ–≤–Ω—ã–µ –ø–æ–ª—è -->
          <div class="info">
            <label>–ò–º—è:</label>
            <input v-model="form.first_name" type="text" />

            <label>–§–∞–º–∏–ª–∏—è:</label>
            <input v-model="form.last_name" type="text" />

            <label>–î–∞—Ç–∞ —Ä–æ–∂–¥–µ–Ω–∏—è:</label>
            <input v-model="form.birth_date" type="date" />

            <label>–î–∞—Ç–∞ –ø—Ä–∏—ë–º–∞:</label>
            <input v-model="form.employ_date" type="date" />

            <label>–ö–∞—Ñ–µ–¥—Ä–∞:</label>
            <select v-model.number="form.department_id">
              <option disabled value="">–í—ã–±–µ—Ä–∏—Ç–µ –∫–∞—Ñ–µ–¥—Ä—É</option>
              <option
                v-for="dep in departments"
                :key="dep.id"
                :value="dep.id"
              >
                {{ dep.name }}
              </option>
            </select>
          </div>

          <!-- –ö–Ω–æ–ø–∫–∏ -->
          <div class="buttons">
            <button class="save-btn" @click="applyUpdate">–ü—Ä–∏–º–µ–Ω–∏—Ç—å</button>
            <button class="close-btn" @click="emit('close')">–û—Ç–º–µ–Ω–∞</button>
          </div>
        </div>
      </div>
    </div>
  </transition>
</template>

<script setup>
import { ref, onMounted } from "vue";
import {
  getInstructorById,
  updateInstructor,
  getInstructorPhoto,
  uploadInstructorPhoto,
  getDepartments,
} from "@/api";

const props = defineProps({
  instructorId: {
    type: Number,
    required: true,
  },
});
const emit = defineEmits(["close", "updated"]);

const form = ref(null);
const loading = ref(false);
const photoPreview = ref(null);
const selectedPhoto = ref(null);
const departments = ref([]);
const originalData = ref(null);

// === –ó–∞–≥—Ä—É–∑–∫–∞ –¥–∞–Ω–Ω—ã—Ö –ø—Ä–µ–ø–æ–¥–∞–≤–∞—Ç–µ–ª—è ===
const loadInstructor = async () => {
  loading.value = true;
  try {
    const { data } = await getInstructorById(props.instructorId);
    form.value = { ...data };
    originalData.value = JSON.parse(JSON.stringify(data)); // –≥–ª—É–±–æ–∫–∞—è –∫–æ–ø–∏—è

    // üì∏ –∑–∞–≥—Ä—É–∂–∞–µ–º —Ñ–æ—Ç–æ –æ—Ç–¥–µ–ª—å–Ω–æ
    try {
      const photoBlob = await getInstructorPhoto(props.instructorId);
      photoPreview.value = URL.createObjectURL(photoBlob);
    } catch {
      photoPreview.value = null;
    }
  } catch (err) {
    console.error("–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –ø—Ä–µ–ø–æ–¥–∞–≤–∞—Ç–µ–ª—è:", err);
  } finally {
    loading.value = false;
  }
};

// === –ó–∞–≥—Ä—É–∑–∫–∞ –∫–∞—Ñ–µ–¥—Ä ===
const loadDepartments = async () => {
  try {
    const { data } = await getDepartments();
    departments.value = data;
  } catch (err) {
    console.error("–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –∫–∞—Ñ–µ–¥—Ä:", err);
  }
};

// === –í—ã–±–æ—Ä –Ω–æ–≤–æ–≥–æ —Ñ–æ—Ç–æ ===
const onPhotoSelected = (e) => {
  const file = e.target.files[0];
  if (!file) return;
  selectedPhoto.value = file;

  const reader = new FileReader();
  reader.onload = (ev) => {
    photoPreview.value = ev.target.result;
  };
  reader.readAsDataURL(file);
};

// === –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –∏–∑–º–µ–Ω–µ–Ω–∏–π ===
const applyUpdate = async () => {
  try {
      // –µ—Å–ª–∏ –≤—ã–±—Ä–∞–Ω–æ –Ω–æ–≤–æ–µ —Ñ–æ—Ç–æ ‚Äî –æ–±–Ω–æ–≤–ª—è–µ–º –µ–≥–æ –æ—Ç–¥–µ–ª—å–Ω–æ
    if (selectedPhoto.value) {
      await uploadInstructorPhoto(props.instructorId, selectedPhoto.value);
    }

  if (JSON.stringify(form.value) === JSON.stringify(originalData.value)) {
      return;  // –Ω–µ –∑–∞—Å–æ—Ä—è–µ–º —Å–µ—Ä–≤–µ—Ä –ª–∏—à–Ω–∏–º–∏ –∑–∞–ø—Ä–æ—Å–∞–º–∏ –ø—Ä–∏ –æ—Ç—Å—É—Ç—Å—Ç–≤–∏–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
    }
    const payload = {
      first_name: form.value.first_name,
      last_name: form.value.last_name,
      birth_date: form.value.birth_date,
      employ_date: form.value.employ_date,
      department_id: form.value.department_id,
    };

    await updateInstructor(props.instructorId, payload);

    alert("‚úÖ –î–∞–Ω–Ω—ã–µ –ø—Ä–µ–ø–æ–¥–∞–≤–∞—Ç–µ–ª—è —É—Å–ø–µ—à–Ω–æ –æ–±–Ω–æ–≤–ª–µ–Ω—ã!");
    emit("updated");
    emit("close");
  } catch (err) {
    console.error("–û—à–∏–±–∫–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –ø—Ä–µ–ø–æ–¥–∞–≤–∞—Ç–µ–ª—è:", err);
  }
};

onMounted(async () => {
  await loadDepartments();
  await loadInstructor();
});
</script>

